function insertOptions(selectedValue){
	
	var reportArray;
	
	if (selectedValue == "-1"){
		alert("Please select a report type whose examples you want to see");
		return false;
	}
	
	
	if (selectedValue == "DSExample"){
		reportArray = reportMappingArray[0];
	} else if(selectedValue == "FormatExample"){
		reportArray = reportMappingArray[1];
	}else if(selectedValue == "PositionExample"){
		reportArray = reportMappingArray[2];
	}else if(selectedValue == "ChartExample"){
		reportArray = reportMappingArray[3];
	}else if(selectedValue == "ScriptExample"){
		reportArray = reportMappingArray[4];
	}else if(selectedValue == "VariableExample"){
		reportArray = reportMappingArray[5];
	} else {
		reportArray = reportMappingArray[6];
	}
	
	var arrayLength = reportArray.length;
		
	var select = document.getElementById("selectReport");
	
	select.options.length = 0;	
	
	for (i=0;i<arrayLength; i++) {
		    //select.add(new Option(reportArray[i][0], reportArray[i][1], false, false));
		    select.options[i] = new Option(reportArray[i][0], reportArray[i][1], false, false);
	}
}